    private void bt_LogIn_IniciarSesionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_LogIn_IniciarSesionMouseClicked
        if (tf_LogIn_Nombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor ingresar el nombre de usuario", "ERROR_USUARIO", JOptionPane.ERROR_MESSAGE);
        } else {
            if (pf_LogIn_Password.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor ingresar la contraseña", "ERROR_CONTRASEÑA", JOptionPane.ERROR_MESSAGE);
            } else {
                boolean usuarioEncontrado = false;
                for (Usuario usuario : usuarios) {
                    if (tf_LogIn_Nombre.getText().equals(usuario.getNombre())) {
                        usuarioEncontrado = true;
                        if (new String(pf_LogIn_Password.getPassword()).equals(usuario.getPassword())) {
                            JOptionPane.showMessageDialog(this, "Ingreso exitoso", "BIENVENIDO", JOptionPane.INFORMATION_MESSAGE);
                            if (tf_LogIn_Nombre.getText().equals("admin") && new String(pf_LogIn_Password.getPassword()).equals("admingrupo5")) {
                                jd_admin.pack();
                                jd_admin.setLocationRelativeTo(null);
                                jd_admin.setSize(750, 450);
                                jd_admin.setVisible(true);
                                this.setVisible(false);
                                tf_LogIn_Nombre.setText("");
                                pf_LogIn_Password.setText("");
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "Contraseña incorrecta", "ERROR_CONTRASEÑA", JOptionPane.ERROR_MESSAGE);
                        }
                        break;
                    }
                }
                if (!usuarioEncontrado) {
                    JOptionPane.showMessageDialog(this, "Nombre de usuario no encontrado", "ERROR_USUARIO", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_bt_LogIn_IniciarSesionMouseClicked

