    private void bt_compraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_compraMouseClicked
        bt_compra.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                int selectedRow = tb_compra_cl.getSelectedRow();
                if (selectedRow != -1) {
                    int id = (int) tb_compra_cl.getValueAt(selectedRow, 0);
                    String tiene_parse = dba.Vista_VerTiene();
                    String[] filas1 = tiene_parse.split("\\|");
                    int cant = 0;
                    for (String fila : filas1) {
                        if (!fila.isEmpty()) {
                            String[] campos = fila.split("&");
                            if (id == Integer.parseInt(campos[0])) {
                                cant = Integer.parseInt(campos[3]);
                            }
                        }
                    }
                    if (cant - 1 == 0) {
                        dba.DeleteTiene(2, id);
                    } else {
                        dba.UpdateTiene(2, id, 500, cant - 1, new String(""));
                    }
                } else {
                    JOptionPane.showMessageDialog(jd_compra, "Por favor, seleccione una fila.", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
        });
    }//GEN-LAST:event_bt_compraMouseClicked

